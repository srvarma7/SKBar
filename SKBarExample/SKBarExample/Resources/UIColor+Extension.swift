import UIKit

extension UIColor {
    static let backgroundPrimary   = UIColor(named: "bgPrimary")!
    static let backgroundSecondary = UIColor(named: "bgSecondary")!
    
    static let textPrimary   = UIColor(named: "textPrimary")!
    static let textPrimaryContrary   = UIColor(named: "textPrimaryContrary")!
    static let textSecondary = UIColor(named: "textSecondary")!
    
    static let accentPrimary = UIColor(named: "accentPrimary")!
}
